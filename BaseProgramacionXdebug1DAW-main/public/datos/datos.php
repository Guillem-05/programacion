function analizarTexto($texto) {
    $numCaracteres = strlen(str_replace([" ", "\n", "\r"], '', $texto));
    $numPalabras = str_word_count($texto);
    $numFrases = substr_count($texto, '.') + substr_count($texto, '!') + substr_count($texto, '?');
    $numParrafos = substr_count($texto, "\n\n") + 1;
    $letrasPorPalabra = $numCaracteres / $numPalabras;
    $palabrasPorFrase = $numPalabras / $numFrases;
    $frasesPorParrafo = $numFrases / $numParrafos;

    return [
        'numCaracteres' => $numCaracteres,
        'numPalabras' => $numPalabras,
        'numFrases' => $numFrases,
        'numParrafos' => $numParrafos,
        'letrasPorPalabra' => $letrasPorPalabra,
        'palabrasPorFrase' => $palabrasPorFrase,
        'frasesPorParrafo' => $frasesPorParrafo,
    ];
}

$texto = "Este es un texto de ejemplo. Aquí hay otra frase. Y un párrafo más.";
$resultados = analizarTexto($texto);
foreach ($resultados as $key => $value) {
    echo ucfirst($key) . ": $value\n";
}







