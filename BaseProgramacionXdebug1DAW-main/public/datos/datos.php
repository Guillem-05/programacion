function buscarPalabra($texto, $palabra) {
    $coincidencias = substr_count($texto, $palabra);
    return $coincidencias > 0 ? "La palabra '$palabra' existe $coincidencias veces." : "La palabra '$palabra' no existe.";
}

function reemplazarPalabra($texto, $palabra) {
    return str_replace($palabra, strtoupper($palabra), $texto);
}

$texto = "Este es un texto de ejemplo para buscar y reemplazar palabras.";
$palabra = readline("Ingrese la palabra a buscar: ");
echo buscarPalabra($texto, $palabra) . "\n";
echo "Texto modificado: " . reemplazarPalabra($texto, $palabra) . "\n";


