function edadHumana($edad) {
    if ($edad <= 2) return $edad * 10.5;
    if ($edad <= 6) return 21 + ($edad - 2) * 4;
    return 21 + ($edad - 6) * 5;
}

function sobrePeso(&$data) {
    $sobrepesoCount = 0;
    foreach ($data as $perro) {
        $edadHumana = edadHumana($perro['edad']);
        if (($edadHumana >= 60 && $perro['peso'] > 7) || 
            ($edadHumana < 40 && $perro['peso'] > 3) || 
            ($edadHumana >= 40 && $edadHumana < 60 && $perro['peso'] > 6)) {
            $sobrepesoCount++;
        }
    }
    return $sobrepesoCount;
}

$totalPerros = count($data);
$sobrepeso = sobrePeso($data);
$porcentajeSobrepeso = ($sobrepeso / $totalPerros) * 100;
echo "Porcentaje de perros con sobrepeso: $porcentajeSobrepeso%\n";


